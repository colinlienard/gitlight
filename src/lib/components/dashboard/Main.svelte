<script lang="ts">
	import { onDestroy, onMount } from 'svelte';
	import { crossfade, slide } from 'svelte/transition';
	import { cubicInOut } from 'svelte/easing';
	import { invoke } from '@tauri-apps/api/tauri';
	import {
		NotificationColumn,
		Settings,
		Separator,
		Banner,
		ScrollbarContainer,
		Tooltip
	} from '~/lib/components';
	import { filteredNotifications, githubNotifications, largeScreen, settings } from '~/lib/stores';
	import {
		CheckIcon,
		GithubIcon,
		GitlabIcon,
		Logo,
		UnreadIcon,
		PinIcon,
		RefreshIcon,
		DoubleCheckIcon
	} from '~/lib/icons';
	import { fetchGithub } from '~/lib/helpers';
	import { browser } from '$app/environment';
	import DoneModal from './DoneModal.svelte';

	export let synced: boolean;

	let syncTime = 0;
	let interval: ReturnType<typeof setInterval>;

	// Filter events
	$: pinned = $filteredNotifications.filter((item) => item.pinned && !item.done);
	$: unread = $filteredNotifications.filter((item) => !item.pinned && item.unread && !item.done);
	$: read = $filteredNotifications.filter((item) => !item.pinned && !item.unread && !item.done);

	$: if (synced && !syncTime) {
		interval = setInterval(() => {
			syncTime += 1;
		}, 1000);
	} else if (!synced) {
		syncTime = 0;
		clearInterval(interval);
	}

	function markAllAsRead() {
		$githubNotifications = $githubNotifications.map((notifications) =>
			unread.includes(notifications) ? { ...notifications, unread: false } : notifications
		);
		fetchGithub('notifications', {
			method: 'PUT',
			body: { read: true }
		});
	}

	function markAllAsDone() {
		$githubNotifications = $githubNotifications.map((notifications) =>
			read.includes(notifications) ? { ...notifications, done: true } : notifications
		);
	}

	// Animations settings
	const animationSettings = { duration: 400, easing: cubicInOut };
	const [send, receive] = crossfade(animationSettings);
	const transitions = { send, receive, settings: animationSettings };

	function handleResize() {
		if (browser && $settings.notificationAxis === 'Auto') {
			$largeScreen = window.innerWidth > 1200;
		}
	}

	$: switch ($settings.notificationAxis) {
		case 'Auto':
			handleResize();
			break;
		case 'Vertical':
			$largeScreen = false;
			break;
		case 'Horizontal':
			$largeScreen = true;
			break;
	}

	$: if (browser && window.__TAURI__ && unread.length === 0) {
		invoke('update_tray', { newIcon: false });
	}

	onMount(() => {
		handleResize();
		window.addEventListener('resize', handleResize);
	});

	onDestroy(() => {
		if (browser) {
			window.removeEventListener('resize', handleResize);
		}
	});
</script>

<main class="main">
	<Banner />
	<header class="header">
		{#if $settings.sidebarHidden}
			<Tooltip content="Show sidebar" position="bottom" hover>
				<button
					class="logo-button"
					on:click={() => ($settings.sidebarHidden = false)}
					transition:slide={{ axis: 'x', duration: 300, easing: cubicInOut }}
				>
					<Logo />
				</button>
			</Tooltip>
		{/if}
		<h1 class="title">Notifications</h1>
		<div class="sync-pill" class:loading={!synced}>
			<RefreshIcon />
			{#if synced}
				Synced {syncTime}s ago
			{:else}
				Syncing...
			{/if}
		</div>
		<Settings />
	</header>
	<nav class="nav">
		<button class="tab selected">
			<GithubIcon />
			<p class="text">GitHub</p>
			<span class="tag">
				<PinIcon />
				{pinned.length}
			</span>
			<span class="tag">
				<UnreadIcon />
				{unread.length}
			</span>
		</button>
		<button class="tab">
			<GitlabIcon />
			<p class="text">GitLab</p>
			<div class="tag soon">Coming soon</div>
		</button>
		<DoneModal />
	</nav>
	<ScrollbarContainer>
		<section class="columns-container" class:horizontal={!$largeScreen}>
			<NotificationColumn
				icon={PinIcon}
				title="Pinned"
				notifications={pinned}
				placeholder={{
					icon: PinIcon,
					text: 'Click on the pin icon to mark a notification as pinned.'
				}}
				{transitions}
			/>
			<Separator vertical={$largeScreen} />
			<NotificationColumn
				icon={UnreadIcon}
				title="Unread"
				notifications={unread}
				placeholder={{ icon: UnreadIcon, text: 'New and unread notifications will appear here.' }}
				{transitions}
			>
				<div slot="header-addon">
					{#if unread.length}
						<button class="read-all" on:click={markAllAsRead}>
							<CheckIcon />
							All
						</button>
					{/if}
				</div>
			</NotificationColumn>
			<Separator vertical={$largeScreen} />
			<NotificationColumn
				icon={CheckIcon}
				title="Read"
				notifications={read}
				placeholder={{
					icon: CheckIcon,
					text: 'Click on the check icon to mark a notification as read.'
				}}
				{transitions}
			>
				<div slot="header-addon">
					{#if read.length}
						<button class="read-all" on:click={markAllAsDone}>
							<DoubleCheckIcon />
							All
						</button>
					{/if}
				</div>
			</NotificationColumn>
		</section>
	</ScrollbarContainer>
</main>

